package triangleMaker;

public class question3proof {

	public static void main(String[] args) {
		//the while looop
		int i = 20;
		while(i>=0)
		{
		            System.out.println(i);
		            i-=5;
		}
		
		System.out.println("\n\n\n********************************\n\n\n");
		
		//the for loop
		for (int j = 20; j>=0; j-=5)
		{
			System.out.println(j);
		}

	}

}
